import turtle
t = turtle.Pen()
red = (1, 0, 0)
green = (0, 1, 0)
blue = (0, 0, 1)
purple = (1, 0, 1)
turquiose = (0, 1, 1)

for i in range(7):
    t.color(red)
    t.forward(100)
    t.backward(100)
    t.right(10)
    t.color(blue)
    t.forward(100)
    t.backward(100)
    t.right(10)
    t.color(green)
    t.forward(100)
    t.backward(100)
    t.right(10)
    t.color(purple)
    t.forward(100)
    t.backward(100)
    t.right(10)
    t.color(turquiose)
    t.forward(100)
    t.backward(100)
    t.right(10)
turtle.exitonclick()


























